	@OTURUM					VARCHAR(36)		= NULL,
	@ID_MENU				INT				= NULL,
	
	@SQLJSON				NVARCHAR(MAX)	= NULL,
	@BASTARIH				VARCHAR(MAX)	= NULL,
	@BITTARIH				VARCHAR(MAX)    = NULL,
	@TC_KULLANICI			VARCHAR(11)		= NULL,
	@DATATABLE				BIT				= NULL

AS
BEGIN
/*

	Oluşturan		: Burhan Akyüz
	Oluşturma Tarih	: 10.11.2020
	
