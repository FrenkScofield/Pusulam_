			SELECT ISNULL((
				SELECT	 ID_DERSPROGRAM
						,DERSSAAT
						,SUBEAD
						,SINIF
						,SD.ID_SINIF
						,SD.ID_SUBE
				FROM OkyanusDB..v3DersProgram	DP
				JOIN OkyanusDB..vw_v4SinifDetay	SD	ON	SD.ID_SINIF = DP.ID_SINIF
													AND SD.DONEM	= OkyanusDB.dbo.fn_AktifDonem()
				WHERE	TCKIMLIKNO	= @TCKIMLIKNO
					AND DP.GUN		= DATEPART(DW,CAST(@TARIH AS DATE))
				ORDER BY DERSSAAT
				FOR JSON PATH, ROOT('LISTE')
			),'{"LISTE":[]}') AS LISTE

