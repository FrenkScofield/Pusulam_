				INNER JOIN OkyanusDB.dbo.v3Kullanici	KL	ON	KL.TCKIMLIKNO	= EK.TCKIMLIKNO
				INNER JOIN OPENJSON(@SQLJSON)
				WITH
				(
					TARIH		NVARCHAR(MAX),
					TCKIMLIKNO	NVARCHAR(MAX),
					ACIKLAMA	NVARCHAR(MAX)
				) S	ON	S.TARIH = E.TARIH	AND S.TCKIMLIKNO = EK.TCKIMLIKNO
				FOR JSON PATH, ROOT('Yoklama')
			), '{ "Yoklama" : [] }') AS LISTE


		END

		IF @ISLEM = 6	--	ÖĞRETMEN DERS PROGRAM LİSTE
